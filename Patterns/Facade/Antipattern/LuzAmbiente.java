public class LuzAmbiente {

    public void ligarLuz() {
        System.out.println("Ligando luz ambiente...");
        System.out.println("Luz ambiente ligada!");
    };

    public void desligarLuz() {
        System.out.println("Desligando luz ambiente...");
        System.out.println("Luz ambiente desligada.");
    };
    
}
